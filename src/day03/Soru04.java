package day03;

import java.util.Scanner;

public class Soru04 {
	public static void main(String[] args) {
		Scanner scan=new Scanner(System.in);
		for(int n=1;n>0;n++) {
		System.out.println("Lutfen 10 ile 0 arasi bir sayi giriniz");
		int sayi=scan.nextInt();
		
		if (sayi>0&&sayi<10) {
			for(int i=1;i<=sayi;i++) {
				System.out.println(i+" "+(i*2)+" "+(i*3)+" "+(i*4));
			}
		}else {
			System.out.println("Gecersiz sayi");
		}
		System.out.println("_________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________________");
		}
	scan.close();}

}
